public class OR_Logical_Operator 
{ 
	public static void main(String[] args)
	{
		int   age    =35;
		float height =7.1f;
		
		if (age!=35 || height!=7.1f)
		{
			System.out.println("You Are Not Fit To Participate");
		}
		 
		else if (age!=35 || height!=7.1f )
		{
			System.out.println(" You Are Not Fit Enough To Participate");
		}
		
		else 
		{
			System.out.println("You Are Fit Enough To Participate");
		}
		

		
	
		
		
		
		
		
		
		
	}
	
	
	
	
	

}
